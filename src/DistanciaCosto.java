
public class DistanciaCosto {

	private int distancia;
	private int nodo;

	public DistanciaCosto(int distancia, int nodo) {
		super();
		this.distancia = distancia;
		this.nodo = nodo;
	}

	public int getDistancia() {
		return distancia;
	}

	public void setDistancia(int distancia) {
		this.distancia = distancia;
	}

	public int getNodo() {
		return nodo;
	}

	public void setNodo(int nodo) {
		this.nodo = nodo;
	}

}
